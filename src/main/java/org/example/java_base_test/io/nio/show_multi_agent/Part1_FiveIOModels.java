        System.out.println("  ★ IO 多路复用 vs BIO 阻塞的本质区别：");
        System.out.println("    BIO：1个线程等1个fd（线程被独占，无法处理其他fd）");
        System.out.println("         → 需要为每个连接创建独立线程，资源消耗大");
        System.out.println("    多路复用：1个线程等N个fd（谁就绪处理谁）");
        System.out.println("         → 少量线程即可管理海量连接，资源利用率高");
