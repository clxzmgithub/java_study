        System.out.println("  read() 返回 ◄──────  返回");
        System.out.println("  特点：两个阶段都阻塞，线程全程等待");
        System.out.println("  🍜类比：你站在前台等外卖，外卖没到你哪也去不了，白白浪费时间");
        System.out.println();

        System.out.println("模型② 非阻塞 IO（NIO 轮询，注意不是 Java NIO！）");
        System.out.println();
        System.out.println("  用户进程             内核");
        System.out.println("  read()  ──────────► 没好，返回 EAGAIN");
        System.out.println("  read()  ──────────► 没好，返回 EAGAIN");
        System.out.println("  read()  ──────────► 就绪！拷贝数据，返回");
        System.out.println("  特点：阶段1不阻塞，但 CPU 一直轮询（忙等，浪费）");
        System.out.println("  🍜类比：每隔30秒跑一趟前台问「外卖到了吗」，没到就走，");
        System.out.println("         来回跑路消耗的精力比干正事还多（CPU空转）");
        System.out.println();

        System.out.println("模型③ IO 多路复用（Java NIO 的 Selector 底层）← 重点！");
        System.out.println();
        System.out.println("  用户进程             内核");
        System.out.println("  epoll_wait ────────► 同时监控 N 个 fd（线程休眠）");
        System.out.println("  （CPU 去干别的）        某个 fd 就绪！唤醒线程");
        System.out.println("  select() 返回 ◄────  返回就绪 fd 列表");
        System.out.println("  read(fd_57) ───────► 拷贝数据（阶段2阻塞）");
        System.out.println();
        System.out.println("  💡 什么是 epoll？为什么它比 select/poll 高效？");
        System.out.println();
        System.out.println("  【select/poll 的问题】需要遍历所有fd才能找出就绪的");
        System.out.println("    应用层：每次调用都要传递所有fd → 内核遍历检查 → 返回位图");
        System.out.println("    应用层：还要再遍历一次位图，找出哪个fd就绪");
        System.out.println("    时间复杂度：O(n)，n是总fd数（即使只有1个就绪，也要遍历全部）");
        System.out.println("    类比：老师问全班50人「谁作业写完了？」，每人都要回答");
        System.out.println();
        System.out.println("  【epoll 的优势】内核维护就绪链表，只返回就绪的fd");
        System.out.println("    初始化：注册fd到内核（只需一次，内核用红黑树保存）");
        System.out.println("    等待时：线程休眠，不占用CPU");
        System.out.println("    数据到达：内核主动将fd加入就绪链表，唤醒线程（通知机制）");
        System.out.println("    返回时：直接返回就绪fd列表，不需要遍历");
        System.out.println("    时间复杂度：O(k)，k是就绪fd数（通常远小于n）");
        System.out.println("    类比：写完作业的学生举手，老师只叫举手的人");
