        System.out.println();
        System.out.println("  模型① BIO  → 你打电话给前台：「我要一份外卖，好了叫我」");
        System.out.println("             然后你就站在前台等，什么都不干，直到外卖到了才走");
        System.out.println("             （线程全程阻塞挂起，无法服务其他连接，线程资源白白占用）");//此时CPU转向执行其他线程，当前线程被阻塞。
        System.out.println("             【关系】1:1（一对一绑定），一个线程独占一个连接");
        System.out.println("             【表现】连接数增加→线程数必须线性增加，阻塞期间不占CPU但占内存");
        System.out.println();
        System.out.println("  模型② NIO轮询 → 你回工位，每隔30秒起来跑一趟前台问：「外卖到了吗？」");
        System.out.println("             没到就回去，30秒后再跑一趟。如此反复。");
        System.out.println("             （CPU 一直忙着轮询，浪费在大量无效的「没到」）");//cpu轮询是因为线程需要不断知道数据是否就绪，于是线程主动调用方法，cpu去判断数据是否就绪。
        System.out.println("             【关系】1:N（但无效消耗高），一个线程管理N个连接但高频无效检查");
        System.out.println("             【表现】即使999个连接无数据，线程依然一次次去问，CPU空转");
        System.out.println();
        System.out.println("  模型③ IO多路复用 → 外卖平台给你一个取餐器（振动手环），");
        System.out.println("             你回工位安心工作，多个外卖同时等，哪个振了去取哪个。");
        System.out.println("             （Selector！1个线程监控N个连接，有就绪才处理）");
        System.out.println("             【关系】M:N（M<<N），少量线程通过Selector监控海量连接");
        System.out.println("             【表现】线程解耦连接，一个线程可管理1万个连接，高性能标准模型");
        System.out.println("             【应用】Redis、Netty、Nginx的基础");
        System.out.println();
        System.out.println("  模型④ 信号驱动 → 你把手机号给餐厅，外卖好了餐厅打你电话通知你");
