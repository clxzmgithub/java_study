        System.out.println();
        System.out.println("  核心优势：1个线程同时等 N 个 fd，有就绪就处理");
        System.out.println("  底层：Linux epoll / macOS kqueue / Windows IOCP");
        System.out.println("  🍜类比：外卖振动手环（Selector），可以同时等10个外卖，");
