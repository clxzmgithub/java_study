        System.out.println("    • 谁在等数据：Selector + 少量线程（休眠等待）");
        System.out.println("    • 谁在搬数据：线程亲自拷贝（阶段2阻塞）");
        System.out.println("    • 典型代表：Netty、Redis、Nginx（高性能网络编程标准）");
        System.out.println();
        System.out.println("  模型④ 信号驱动：");
        System.out.println("    • 线程:连接 = 分阶段变化（通知阶段一对多，搬运阶段1:1）");
        System.out.println("    • 谁在等数据：内核通知，线程收到信号后处理");
        System.out.println("    • 谁在搬数据：线程亲自拷贝（阶段2阻塞）");
        System.out.println("    • 典型代表：极少用于高并发场景");
